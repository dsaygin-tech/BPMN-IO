export function menuT(key, locale = 'en', replacements = {}) {
  const template = MENU_I18N[locale]?.[key] ?? MENU_I18N.en[key] ?? key;

  return template.replace(/\{([^}]+)\}/g, (_, name) => (
    replacements[name] ?? `{${name}}`
  ));
}
